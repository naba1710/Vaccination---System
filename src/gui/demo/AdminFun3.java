    private void modifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyActionPerformed
        int i = Table.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)Table.getModel();
        if (i >= 0)
        {

            model.setValueAt(txtCcode.getText(), i, 1);
            model.setValueAt(txtCname.getText(), i, 2);
            model.setValueAt(txtVname.getText(), i, 3);
            model.setValueAt(txtVamount.getText(), i, 4);
            model.setValueAt(txtOpen.getText(), i, 5);
            model.setValueAt(txtClose.getText(), i, 6);


        }
        else
        {
            JOptionPane.showMessageDialog(null, "Error");
        }  // TODO add your handling code here:
    }//GEN-LAST:event_modifyActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            File file = new File ("Supply.txt");
            if(!file.exists()){
                file.createNewFile();
            }

            FileWriter fw = new FileWriter(file.getAbsoluteFile());
            BufferedWriter bw = new BufferedWriter(fw);
            for (int i = 0; i < Table.getRowCount(); i++){
                for(int j = 0; j< Table.getColumnCount(); j++){
                    bw.write(Table.getModel().getValueAt(i,j)+",");
                }
                bw.write("\n");
            }
            bw.close();
            fw.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error, please retry");
        }   // TODO add your handling code here:
    }//GEN-LAST:event_updateActionPerformed

